/*!
# The [PartialOrd] trait.

Trait for types that form a [partial order].

> <i>"a partial order on a set is an arrangement such that, for certain pairs of elements, one
> precedes the other. The word partial is used to indicate that not every pair of elements needs to
> be comparable; that is, there may be pairs for which neither element precedes the other."</i>

Properties:

-   Reflexive
-   Transitive
-   Antisymmetric

-   See: <https://doc.rust-lang.org/std/cmp/trait.PartialOrd.html>
-   See: <https://en.wikipedia.org/wiki/Partially_ordered_set>

[partial order]: https://en.wikipedia.org/wiki/Partially_ordered_set
 */
